/**
 * Base LLM Service - Abstracts provider implementation
 */
export abstract class BaseLLMService implements LLMProvider {
  abstract readonly name: string
  protected apiKey: string
  protected baseUrl: string

  constructor(apiKey: string, baseUrl: string) {
    this.apiKey = apiKey
    this.baseUrl = baseUrl
  }

  abstract generate(prompt: string, options?: GenerationOptions): Promise<LLMResponse>
  abstract chat(messages: Array<Message>, options?: GenerationOptions): Promise<LLMResponse>

  /**
   * Helper method to create system message
   */
  protected createSystemMessage(context: string): Message {
    return {
      role: 'system',
      content: `You are an expert career advisor and resume consultant. Your goal is to help users create outstanding, professional CVs that effectively showcase their skills and experience. Be constructive, specific, and encouraging in your feedback.`,
    }
  }

  /**
   * Helper method to validate response
   */
  protected validateResponse(response: LLMResponse): boolean {
    return !!(response.content && response.content.trim().length > 0)
  }
}

