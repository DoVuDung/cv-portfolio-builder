/**
 * LLM Provider Interface
 */
export interface LLMProvider {
  name: string
  generate(prompt: string, options?: GenerationOptions): Promise<LLMResponse>
  chat(messages: Array<Message>, options?: GenerationOptions): Promise<LLMResponse>
}

export interface Message {
  role: 'system' | 'user' | 'assistant'
  content: string
}

export interface GenerationOptions {
  temperature?: number
  maxTokens?: number
  topP?: number
  frequencyPenalty?: number
  presencePenalty?: number
  stopSequences?: Array<string>
}

export interface LLMResponse {
  content: string
  usage?: {
    promptTokens: number
    completionTokens: number
    totalTokens: number
  }
  finishReason?: 'stop' | 'length' | 'content_filter'
}

/**
 * Prompt Templates
 */
export class PromptTemplate {
  private template: string
  private variables: Set<string>

  constructor(template: string) {
    this.template = template
    this.variables = new Set(
      template.match(/\{\{(\w+)\}\}/g)?.map(m => m.slice(2, -2)) || []
    )
  }

  format(values: Record<string, string>): string {
    let result = this.template
    
    for (const variable of this.variables) {
      const value = values[variable]
      if (!value) {
        throw new Error(`Missing required variable: ${variable}`)
      }
      result = result.replace(new RegExp(`\\{\\{${variable}\\}\\}`, 'g'), value)
    }
    
    return result
  }
}

